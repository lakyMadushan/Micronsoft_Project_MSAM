    private void idLoad() {
        try {
            ResultSet rs = DB.search("select id from customer order by id DESC");
            if (rs.next()) {
                jTextField1.setText(String.valueOf(Integer.parseInt(rs.getString("id")) + 1));
            } else {
                jTextField1.setText("1");
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }

    private void coun_comboLoad() {
        try {
            jComboBox1.removeAllItems();
            ResultSet rs = DB.search("select * from country order by country");
            while (rs.next()) {
                jComboBox1.addItem(rs.getString("country"));
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }

    private void tableLoad() {
        try {
            clear();
            DefaultTableModel dtm = (DefaultTableModel) jTable1.getModel();
            dtm.setRowCount(0);
            ResultSet rs = DB.search("select * from customer");
            while (rs.next()) {
                Vector<Object> v = new Vector<>();
                v.add(rs.getString("id"));
                v.add(rs.getString("name"));
                v.add(rs.getString("country"));
                v.add(rs.getString("contact"));
                v.add(rs.getString("email"));
                dtm.addRow(v);
            }
            coun_comboLoad();
            jButton1.setEnabled(true);
        } catch (Exception e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "Table loading Unsuccesfully !", "ERROR", JOptionPane.INFORMATION_MESSAGE, erricon);
        }
    }

    private void clear() {
        // Clear
        jTextField2.setText("");
        jTextField3.setText("");
        jTextField4.setText("");
        ((DefaultTableModel) jTable1.getModel()).setNumRows(0);
        jButton1.setEnabled(true);
        coun_comboLoad();
        idLoad();
    }
    
