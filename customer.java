    private void jTextField4KeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jTextField4KeyPressed
        if (evt.getKeyCode() == KeyEvent.VK_ENTER || evt.getKeyCode() == KeyEvent.VK_RIGHT) {
            if (jTextField4.getText().contains("@") && jTextField4.getText().contains(".")) {
                jButton1.requestFocus();

            } else {
                JOptionPane.showMessageDialog(this, "Invalid E-mail", "ERROR!", JOptionPane.ERROR_MESSAGE, erricon);
                jTextField4.setText("");
                jTextField4.grabFocus();
            }
        } else if (evt.getKeyCode() == KeyEvent.VK_LEFT) {
            jTextField3.grabFocus();
        }
    }//GEN-LAST:event_jTextField4KeyPressed

