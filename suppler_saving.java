    private void jTextField11KeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jTextField11KeyReleased
        if (jTextField11.getText().isEmpty()) {
            jTextField1.setText("");
            jTextField2.setText("");
            jTextField3.setText("");
            jTextField4.setText("");
            jButton1.setEnabled(true);
            jButton2.setEnabled(true);
            jButton5.setEnabled(true);
        } else {
            try {
                jButton1.setEnabled(false);
                jButton2.setEnabled(false);
                jButton5.setEnabled(false);
                jTextField2.setText("");
                jTextField3.setText("");
                jTextField4.setText("");

                ResultSet rs = DB.search("select * from grn where suppler like '%" + jTextField11.getText() + "%'");
                ResultSet rs2 = DB.search("select * from suppler where s_id like '%" + jTextField11.getText() + "%'");
                ResultSet rs3 = DB.search("select sum(due) from grn where suppler like '%" + jTextField11.getText() + "%'");

                Vector v1 = new Vector();
                while (rs2.next() && rs3.next() && rs.next()) {

                    double sum = rs3.getDouble("sum(due)");
                    
                    if (!rs.getString("suppler").equals(jTextField11.getText())) {
                        if (sum < 0) {
                            if (!rs.getString("suppler").equals(jTextField11.getText())) {
                                v1.add(rs2.getString("id") + ":" + rs2.getString("name") + "-" + rs2.getString("contact_no") + rs3.getString("sum(due)"));
                            }
                            JOptionPane.showMessageDialog(this, "You must Pay all : " + rs.getString("suppler") + " : Ru. " + String.valueOf(sum), "ERROR", JOptionPane.ERROR_MESSAGE, erricon);
                        } else {
                            if (!rs.getString("suppler").equals(jTextField11.getText())) {
                                v1.add(rs2.getString("id") + ":" + rs2.getString("name") + "-" + rs2.getString("contact_no") + "-" + rs3.getString("sum(due)"));
                            }
                        }
                    } else {
                        JOptionPane.showMessageDialog(this, "This suppler not here !!!" , "ERROR", JOptionPane.ERROR_MESSAGE, erricon);
                    }

                }
                jList1.setListData(v1);

            } catch (Exception e) {
                e.printStackTrace();
            }
        }

    }//GEN-LAST:event_jTextField11KeyReleased

